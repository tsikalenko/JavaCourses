package lection4;

public class Lection4HomeWorkTask3 {

	public static void main(String[] args) {
		for (int i = 0; i < 5; i++) {
			for (int j = 0; j < 5; j++) {
				System.out.print((j + 1) + " x " + (i + 1) + " = " + ((i + 1) * (j + 1)) + "   ");
			}
			System.out.println();
		}
	}

}
